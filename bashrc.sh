rp() # show real path of file or directory ++ usage: $ rp ${file/dir}
{
	cd "$(dirname $1)" # go to file directory
	echo "$(pwd)/$(basename $1)"
}

# ottenere il path di un file relativo alla directory corrente
rpr() # usage: $ realpath ${file}
{
	file_path=$1
	current_directory=$(pwd)
	# Calculate the relative path using Python
	relative_path=$(python -c "import os; print(os.path.relpath('$file_path', '$current_directory'))")
	echo "Relative path: $relative_path"
}



